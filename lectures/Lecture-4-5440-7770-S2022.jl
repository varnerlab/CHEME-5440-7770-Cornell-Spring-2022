# ‚ïî‚ïê‚ï° 67e69515-6d22-4693-85e0-c3767e5b9a06
begin

	# what is the equilibrium extent?
	œµ_scaled = bgfs_soln;
	œµ = sum(initial_mol_array)*œµ_scaled
	
	# compute the equlibrium constant -
	n_final = initial_mol_array + stoichiometric_array*œµ

	# compute the final mol fraction -
	ln_x_final = log.((1/sum(n_final)).*n_final)

	# compute the Keq -
	tmp = dot(stoichiometric_array,ln_x_final)
	K_eq = exp(tmp)

	with_terminal() do
		println("Estimated Keq = $(K_eq)")
	end
end

