# ‚ïî‚ïê‚ï° a4a028a8-6ffb-4849-8b4a-e41abd6290f7
begin
	
	# Use the answer from Method 2 as a starting point 
	xinitial = [0.65]
	
	# setup bounds -
	L = 0.00001
	U = 0.99999
	
	# setup the objective function -
	OF(p) = objective_function(p, parameters_dict)
    
    # call the optimizer -
    opt_result = optimize(OF,L, U, xinitial, Fminbox(BFGS()))
end

# ‚ïî‚ïê‚ï° ce55c19c-1137-4801-81c1-bf2ff07c0480
begin
	bgfs_soln = Optim.minimizer(opt_result)[1]
	with_terminal() do
		println("Optim found œµ (scaled) = $(bgfs_soln) AU")
	end
