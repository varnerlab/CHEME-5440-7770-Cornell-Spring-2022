###### Method 2: Constrained nonlinear optimization problem
Let's recast the Gibbs energy estimation as a (nonlinear) _constrained_ optimization problem and solve this problem using the [Optim.jl](https://github.com/JuliaNLSolvers/Optim.jl) package. 
