# ‚ïî‚ïê‚ï° 21718432-f5b5-4886-b04b-a00abfd451f4
begin
	
	# initialize -
	number_of_steps = 200
	gibbs_energy_array = Array{Float64,1}()
	epsilon_range = range(0.00001,stop=0.99999,length=number_of_steps) |> collect
	
	# compute -
	for œµ in epsilon_range
		
		gibbs_energy_value = objective_function([œµ],parameters_dict);
		push!(gibbs_energy_array,gibbs_energy_value)
	end
