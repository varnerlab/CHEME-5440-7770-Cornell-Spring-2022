begin
	
	# number of species and reactions -
	‚Ñ≥ = 2
	‚Ñõ = 1
	
	# setup the initial number of mols -
	initial_mol_array = zeros(‚Ñ≥,1)
	initial_mol_array[1,1] = 22.0 	# units: mmol G6P = 1
	initial_mol_array[2,1] = 0.0 	# units: mmol F6P = 2
	
